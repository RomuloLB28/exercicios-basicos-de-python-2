#leia um número e imprima a quinta parte dele

num = float(input("Digite um número\n"))
num1 = num/5

print(f'A quinta parte do número digitado é: ',round(num1,2))
#obs: round(num1) -> arredonda para o inteiro mais proximos,  round(num1,2)-> arredonda o número até as duas casas décimais após a vírgula. exemplo: 6.33333333333 fica 6.34

